CREATE OR REPLACE TABLE DATA_ACADEMY.SNEAKER_FACTORY.CLEANED_INFLATION_SQL
AS

WITH CTE AS 
(
SELECT 
    country_name
    , "2000" ,"2001","2002", "2003","2004","2005","2006", "2007","2008","2009","2010", "2011","2012","2013","2014", "2015","2016","2017","2018", "2019","2020","2021"
FROM INFLATION_INFO
WHERE COUNTRY_NAME IN ('Netherlands','United Kingdom', 'Italy','Sweden', 'Poland')
)

SELECT * 
FROM CTE
UNPIVOT( INFLATION_PERC for YEAR in ("2000" ,"2001","2002", "2003","2004","2005","2006", "2007","2008","2009","2010", "2011","2012","2013","2014", "2015","2016","2017","2018", "2019","2020","2021"))
ORDER BY COUNTRY_NAME, YEAR